# 헬퍼 함수
# ────────────────────────────────────────────────────────────

def classify_obligation_strength(sentence: str) -> str:
    """문장의 의무 강도 분류.

    Returns: 'strong' / 'weak' / 'declarative' / 'imperative' / 'interrogative' / 'none'
    """
    if STRONG_OBLIGATION.search(sentence): return 'strong'
    if WEAK_OBLIGATION.search(sentence): return 'weak'
    if IMPERATIVE_END.search(sentence): return 'imperative'
    if INTERROGATIVE_END.search(sentence): return 'interrogative'
    if DECLARATIVE_END.search(sentence): return 'declarative'
    return 'none'


def has_actor_keyword(text: str) -> bool:
    """주체 키워드(시스템·관리자·제안사 등) 존재 여부."""
    return ACTOR_KEYWORDS.search(text) is not None


def find_vague_terms(sentence: str) -> list:
    """문장에서 모호어 매칭 결과 반환 (카테고리 + 어휘)."""
    hits = []
    for term in VAGUE_LEXICAL:
        if term in sentence: hits.append(('lexical', term))
    for term in VAGUE_QUANTIFIER:
        if term in sentence: hits.append(('quantifier', term))
    for term in VAGUE_TIME:
        if term in sentence: hits.append(('time', term))
    return hits


def has_passive_marker(sentence: str) -> bool:
    return PASSIVE_EXPRESSION.search(sentence) is not None


def has_translationese(sentence: str) -> bool:
    """번역체 흔적 — RFP 품질 신호로 활용"""
    return TRANSLATIONESE.search(sentence) is not None


def has_double_negation(sentence: str) -> bool:
    return DOUBLE_NEGATION.search(sentence) is not None


def has_quantitative_unit(sentence: str) -> bool:
    """한국어 수량 단위 존재 여부."""
    return KOREAN_QUANTITY_UNITS.search(sentence) is not None


def is_qualitative_without_metric(sentence: str) -> bool:
    """정성 표현 + 정량 단위 부재 — S10 검증불가."""
    return (QUALITATIVE_NO_METRIC.search(sentence) is not None
            and not has_quantitative_unit(sentence))
