"""한국어 RFP 특화 패턴 사전 — v2.6 고도화 모듈.

기존 detectors가 정규식을 산재시킨 것을 한 곳에 모아 카테고리화:

A. 종결어미 분류 (의무 강도 5단계)
B. 격조사·보조사 분류 (주체/대상/한정)
C. 의무·금지·허용 modality
D. 모호어 (어휘 모호 / 양화사 모호 / 시간 모호 / 정도 모호)
E. 한국 RFP 도메인 어휘
F. 부정·이중부정 패턴
G. 번역체 표현
H. 한자어 의무 표현

각 detectors는 본 모듈을 import 해 사용.
"""
import re


# ────────────────────────────────────────────────────────────
# A. 종결어미 — 의무 강도 분류 (5단계)
# ────────────────────────────────────────────────────────────

# A1. 강한 의무 (Strong Modality)
STRONG_OBLIGATION = re.compile(
    r'(?:'
    r'(?:하|되|이|있)여야\s*(?:한다|함|됨)'
    r'|해야\s*(?:한다|함)'
    r'|할\s*수\s*있어야\s*(?:한다|함)'
    r'|반드시\s+[가-힣]+'
    r'|필수\s*(?:이다|로|적|항목)'
    r'|의무\s*(?:이다|적|로|화)'
    r'|[가-힣]+토록\s*한다'
    r'|함이\s*마땅\s*하다'
    r'|함이\s*적절\s*하다'
    r')'
)

# A2. 권고·약한 의무
WEAK_OBLIGATION = re.compile(
    r'(?:'
    r'권장\s*(?:한다|됨|함|된다)'
    r'|권고\s*(?:한다|됨|함|된다)'
    r'|바람직\s*(?:하다|함)'
    r'|함이\s*좋다'
    r'|함이\s*바람직'
    r'|~할\s*수\s*있다'
    r'|가능\s*(?:하다|함|할\s*수\s*있)'
    r')'
)

# A3. 평서형 종결 (의무 표시 없음)
DECLARATIVE_END = re.compile(
    r'(?:'
    r'(?<=[가-힣])(?:한다|된다|이다|있다|없다)\.?\s*$'
    r'|(?<=[가-힣])(?:함|됨)\.?\s*$'
    r'|(?<=[가-힣])(?:다|음)\.?\s*$'
    r')'
)

# A4. 명령형/청유형 (드물지만 RFP에 등장)
IMPERATIVE_END = re.compile(
    r'(?:'
    r'하라\.?\s*$|하시오\.?\s*$|하십시오\.?\s*$'
    r'|하자\.?\s*$'
    r')'
)

# A5. 의문형 (요구사항 아닌 신호)
INTERROGATIVE_END = re.compile(
    r'(?:'
    r'인가\?|있는가\?|되는가\?|할\s*수\s*있는가\?'
    r')'
)


# ────────────────────────────────────────────────────────────
# B. 격조사·보조사 분류
# ────────────────────────────────────────────────────────────

# B1. 주격 조사 (Nominative)
NOM_PARTICLE = re.compile(r'[가-힣]+(이|가|께서)\s')

# B2. 보조사 (주체 후보로도 작동)
TOPIC_PARTICLE = re.compile(r'[가-힣]+(은|는|도|만|조차|마저)\s')

# B3. 목적격 조사 (Accusative)
ACC_PARTICLE = re.compile(r'[가-힣]+(을|를)\s')

# B4. 처격·여격 (장소·대상)
LOC_PARTICLE = re.compile(r'[가-힣]+(에|에서|에게|으로|로|로서|로써)\s')

# B5. 관형격 (Genitive)
GEN_PARTICLE = re.compile(r'[가-힣]+의\s')


# ────────────────────────────────────────────────────────────
# C. 부정 표현 (Negation)
# ────────────────────────────────────────────────────────────

NEGATION = re.compile(
    r'(?:'
    r'하지\s*않\s*(?:는|음|아|어|는다)'
    r'|되지\s*않\s*(?:는|음|아|어|는다)'
    r'|할\s*수\s*없\s*(?:다|음|는)'
    r'|아니\s*(?:다|함|면)'
    r'|불가\s*(?:능|하다|함)'
    r'|미\s*(?:비|시행|완료|충족)'
    r')'
)

# 이중부정 (혼동 유발)
DOUBLE_NEGATION = re.compile(
    r'(?:'
    r'없지\s*않'
    r'|아닌\s*것은\s*아니'
    r'|불가능\s*하지\s*않'
    r'|않을\s*수\s*없'
    r')'
)


